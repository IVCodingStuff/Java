package fundamentos;

public class ex_003 {

	public static void main(String[] args) {
		int a = 10;
		int b = -10;
		boolean c = true;
		boolean d = false;
		System.out.println(~a); // -11 (inverte o sinal e subtrai 1)
		System.out.println(~b);
		System.out.println(!c); // false (oposto ao valor booleano)
		System.out.println(!d); // true
		// and -> &&
		// or -> || 
		// xor -> ^
		//	not -> !
	}

}